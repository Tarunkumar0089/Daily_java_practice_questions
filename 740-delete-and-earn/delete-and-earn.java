class Solution {
    int[] points;
    int[] dp;
    public int deleteAndEarn(int[] nums) {
        int max = 0;
        for (int x : nums) max = Math.max(max, x);
        points = new int[max + 1];
        for (int x : nums) {
            points[x] += x;
        }
        dp = new int[max + 1];
        Arrays.fill(dp, -1);

        return solve(max);
    }

    public int solve(int i) {
        if (i <= 0) return 0;
        if (dp[i] != -1) return dp[i];
        int pick = points[i] + solve(i - 2);
        int notPick = solve(i - 1);

        return dp[i] = Math.max(pick, notPick);
    }
}
